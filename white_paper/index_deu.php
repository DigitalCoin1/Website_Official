<p id="PROBLEM"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>DAS PROBLEM</h2></b></u><br>
				Denken Sie an eine Welt, in der Sie Ihre Lieblingsprodukte, Ihr Lieblingsessen kaufen und sogar erobern können. Sie müssen nicht den Preis bezahlen, der für diesen Artikel oder Service mit Ihrer aktuellen Landeswährung aufgewendet wird...<br>
				Wäre das nicht interessant?<br>
				Seien Sie in der Lage, für das Abendessen Ihrer Frau zu zahlen, kaufen Sie das Spielzeug oder den Snack Ihres Kindes an der Tür Ihrer Schule oder arbeiten Sie, ohne mit Ihrem Monatsbudget herumspielen zu müssen.<br>
				Dies ist seit dem Jahr 2009 durch ein von Satoshi Nakamoto weltweit bekanntes dezentrales Währungssystem möglich, das von einem (oder einem Team) erstellt wurde. Es wurde aus einem Quellcode erstellt, der von diesem Wesen (oder Team) entwickelt wurde
ein völlig anderes Bankensystem als das herkömmliche, wo alle Transaktionen ohne Zwischen- oder Bankverspätungen aufgrund ihrer Arbeitszeit stattfinden.<br>
				BITCOIN wurde geschaffen, um all diese Bedürfnisse zu erfüllen und das Beste: Es war kostenlos erhältlich!<br>
				Jeder mit einem Computer, der mit dem Internet verbunden ist, könnte den bekannten Abbau durchführen und im Austausch für das Ergebnis des Arbeitstests einige Münzen erhalten, die von diesem System bereitgestellt werden.<br>
				Mining bestand darin, mathematische Probleme zu lösen, und diese Auflösung wurde durch Prozessoren erreicht, die extrem genutzt wurden, um Geschwindigkeit und genaue Antworten zu gewährleisten.
Hier beginnt das erste Problem: <br>
Im Laufe der Jahre wurde die Währung immer bekannter und immer mehr Nutzer nutzten dieses Medium. Mit dem Wachstum der Nutzer wird die Beschaffung jedoch immer schwieriger, weil es immer
Verbesserungen bei Prozessorfertigungen und damit älteren Prozessoren und Veralten. <br>
Dann begann das Rennen um Münzen zu erhalten!
Je neuer Ihr Prozessor und je besser Ihre Verarbeitungsleistung, desto schneller erhalten Sie den Satoshis (BITCOIN Fractional Unit). Mit diesem Rennen begann der Marktpreis zu hebeln und jeder
mehr und mehr Benutzer kamen an.<br>
				Hier beginnt also eine neue Modalität: die Beteiligung von Unternehmen für den professionellen Bergbau (ASIC).<br>
Firmen begannen, Maschinen mit unglaublich starker Hardware zu entwickeln und setzten absurde Preise für ihren Erwerb auf.<br>
Viele Nutzer fingen an, diese Ausrüstungen zu kaufen, und diejenigen, die diese Bedingungen nicht hatten, fingen an, Programme wie "Cloud-Mining" oder Bergbau in der Cloud einzugeben, wo die großen Unternehmen zahlreiche ASICs kauften und
damit dienten sie den Nutzern die Miete der Verarbeitungsmacht.<br>
				 Das Gegenstück zu dieser Regelung war in erster Linie das sehr hohe Risiko, einen Service zu mieten, der niemals eine Rendite bringen würde, und im Gegenzug, wenn man die Ausrüstung kaufte, war der Energieverbrauch sehr hoch.
macht oft Bergbauschäden.<br>
				Um eine Idee zu bekommen, kostet ein ASIC S9 durchschnittlich $ 2200 bei einem Verbrauch von 1300W! Damit haben wir den Verbrauch einer Klimaanlage (1200W) überlegen !!!<br>
Auf diese Weise haben wir manchmal professionelle Unternehmen mit mehr als 50 Maschinen gleichzeitig verbunden ... Der Verbrauch von elektrischer Energie ist absurd und ökologisch gesehen völlig untragbar.<br>
				<br><p><img src="img/bitcoinfarm.jpg"><br><font style="font-size:12px">Professionelles Bergbauzentrum</font><br></p>
				So werden nur die Reichen reicher, verschwenden ihre hohen Kosten, die für viele Male nicht bezahlt werden, und der Endbenutzer ist total der Gnade dieser Entitäten ausgeliefert.<br>
Ein weiteres schwerwiegendes Problem, das mit der Zunahme der Anzahl der Benutzer des BITCOIN-Netzwerks gemeldet und erlebt wurde, war die Verzögerung der Bestätigung einer Transaktion oder sogar die Hinzufügung dieser Transaktion in Blockchain.<br>
Ein BITCOIN-Block kann bis zu ca. 1,02 Megabyte (Tag) oder fast 292.359 Transaktionen pro Tag empfangen. Um Ihre Transaktion an diesen Block zu senden und der Blockchain hinzuzufügen, zahlen Sie eine Gebühr, die als "Gebühr" bezeichnet wird. Die Rate
BITCOIN Standard ist heute 0,00001BTC, und wenn Sie eine höhere Rate zahlen, hat Ihre Transaktion eine höhere Priorität als die anderen und wird schneller zum Block hinzugefügt.<br>
				Wenn dies der Fall wäre, wenn der Block unter der Nummer 292,358 wäre und Ihre Transaktion diejenige war, die den Block geschlossen hat und jemand eine höhere Rate als Ihre gesandt hat, würde Ihre Transaktion nur in den nächsten Block eintreten.
Damit ist der Aufwand mit hohen Raten ökonomisch oft nicht tragbar und beim Senden mit Standardtarif ist die Wartezeit gigantisch (es kann mehr als 1 Tag dauern).<br>
