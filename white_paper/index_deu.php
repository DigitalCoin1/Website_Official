<p id="SOLUTION"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>DIE LÖSUNG</h2></b></u><br>
				SPERO hat die Lücken des ursprünglichen BITCOIN-Vorschlags geschlossen. <br>
Wir führen das PoS-Mining oder den Pfahlnachweis ein, bei dem das Netzwerk ein Portfolio auswählt und einen Block darin erstellt. Beim Erstellen des Blocks erhalten Sie eine Belohnung für die Teilnahme an der Transaktion. <br>
Auf diese Weise steigt die Wahrscheinlichkeit, ausgewählt zu werden, umso größer die Balance. <br>
Die von SPERO gezahlte Prämie entspricht einem Anteil von <b>25%</b> pro Jahr am Gesamtwert des Portfolios. <br>
Auf diese Weise benötigen Sie nur einen Computer, auf dem SPERO-Software ausgeführt wird, und halten Sie sich an einige einfache Regeln: <br>
				<br><p style="text-align:left;">→ Halten Sie Ihren Geldbeutel mindestens 2 Stunden täglich und maximal 12 Stunden online. <br>
→ Halten Sie Ihre Brieftasche für die Teilnahme freigeschaltet; <br>
→ Je höher Ihr Kontostand ist, desto schneller können Sie durch die Auslosung des Netzwerks berücksichtigt werden.<br>
→ Der Saldo entspricht Ihrem "Gewicht" des Netzwerks;<br>
→ Saldo größer als 1 SPERO <br> </p>
				Die Vorteile dieser Art des Bergbaus sind zahlreich, und das Beste ist die Möglichkeit, an diesem Bergbau teilzunehmen, ohne Geld für Ausrüstung mit hohen Preisen auszugeben. Sie können verwenden
der alte Pentium oder ein RaspBerry zum Beispiel!<br>
Der zweite Vorteil: Der geringe Stromverbrauch. <br><br>
Zunächst schlagen wir die Verteilung der Phasen unseres Projekts vor, aufgeteilt in die Anzahl der Blöcke: <br>
• Von Block 0 bis 33331 = PoW- und PoS-Mining-Phase; <br>
• Von Block 33332 nach 263250 = PoS-Mining-Phase; <br>
• ab Block 263251 oben = PoW- und PoS-Mining-Phase<br>
				<br>
				Darüber hinaus haben wir den Vorteil eines skalierbaren oder verstellbaren Blocks je nach Bedarf des Netzwerks. Denken Sie an das Beispiel von BITCOIN? Dafür haben unsere Blöcke maximal 60 Sekunden Zeit und danach ist im Buch festgehalten.
Wenn das Netzwerk innerhalb von 60 Sekunden 292.360 Transaktionen oder mehr als 20 MB verwenden muss, werden alle Transaktionen gesendet! <br>
Damit gewinnen wir schnell an Agilität bei Transaktionen und Vergütungen: <br><br>
<p> • 03 Bestätigungen für Benutzer-zu-Benutzer-Transaktionen;<br>
• 05 Bestätigungen für abgebaute Transaktionen <br> </p>
Somit haben die Transaktionen eine maximale Zeit von 5 Minuten, die in dem Block in der PoS-Mining-Phase und weniger als 3 Min in der Hybrid-Mining-Phase aufgezeichnet werden sollen.<br>
