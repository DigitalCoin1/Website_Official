<p id="PROBLEM"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>問題</h2></b></u><br>
				想想一個你可以購買你最喜歡的產品，你最喜歡的食物，甚至征服它的世界，你不必花費在目前的國家貨幣...<br>
				這不是很有趣嗎？<br>
				能夠支付你妻子的晚餐，在你的學校或工作的門口買你的孩子的玩具或小吃，而不必修改你的每月預算。<br>
				自2009年以來，通過由中本聰全球知名的一個（或一個團隊）創建的分散化貨幣體系，這是可能的。 它是由這個（或團隊）開發的源代碼創建的，
與傳統銀行系統完全不同的是，所有的交易都是在工作時間內沒有任何中介或銀行延誤的情況下進行的。<br>
				BITCOIN的創建是為了滿足所有這些需求，最重要的是：免費提供！<br>
				任何人連接到互聯網的計算機可以執行已知的挖掘，並獲得勞動測試的結果，由該系統提供一些硬幣。<br>
				挖掘由解決數學問題組成，這個解決方案是通過處理器來完成的，這些處理器被用來確保速度和確切的答案。<br>
				這裡開始了第一個問題：<br>
				多年來，人們越來越了解貨幣，越來越多的用戶開始使用這種媒介。 然而，隨著用戶的增長，採購變得越來越困難，因為總是有
改進處理器的製造，從而使舊的處理器變得過時。<br>
				然後開始比賽獲得金幣！<br>
				處理器越新，處理能力越好，Satoshis（BITCOIN Fractional Unit）的速度越快。 在這場比賽中，市場價格開始與槓桿相關
越來越多的用戶到達.<br>
				在這裡，一個新的模式開始：專業採礦公司（ASIC）的參與。<br>
				公司開始開發機器硬件非常強大，收取荒謬的價格收購.<br>
				許多用戶開始購買這些設備和那些不具備條件的用戶，開始進入被稱為“雲開採”或在雲中開采的方案，大公司購買了大量的ASIC和
與此同時，他們向用戶提供了處理權力的租金。<br>
				 這個計劃的副本首先是僱用一個永遠不會給你任何回報的服務的非常高的風險，作為回報，如果你購買設備的能源消耗非常高，
經常造成採礦破壞.<br>
				為了得到一個想法，ASIC S9的平均成本為2200美元，功耗為1300W！ 有了這個，我們的消費優於空調（1200W）！<br>
				這樣，我們有時候會有專業公司同時連接50多台機器......電能的消耗是荒謬的，在生態上完全不可持續.<br>
				<br><p><img src="img/bitcoinfarm.jpg"><br><font style="font-size:12px">專業採礦中心</font><br></p>
				因此，只有富人變得更富有，浪費了昂貴的成本，而這些昂貴的成本往往沒有得到支付，最終用戶完全處於這些實體的擺佈之下.<br>
				BITCOIN網絡用戶數量的增長所引發的另一個主要問題是延遲確認交易，甚至是在區塊鏈中增加了這筆交易。<br>
				一個BITCOIN塊最多可以接收大約1.02兆字節（天）或幾乎每天292,359筆交易。 要將您的交易發送到此區塊，並將其添加到區塊鏈，您需要支付“費用”費用。 率
今天的BITCOIN標準是0.00001BTC，如果你支付更高的價格，你的交易比其他人有更高的優先級，並將被加入到更快的塊。<br>
				建議如果這個區塊的數量是292358，而你的交易是關閉區塊的交易，而有人發送的價格高於你的交易，你的交易只會進入下一個區塊.<br>
				因此，高利率的費用在經濟上是不可持續的，以標準費率發送時，等待時間是巨大的（可能需要1天以上）。<br>
