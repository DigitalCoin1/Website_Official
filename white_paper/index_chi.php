<p id="SOLUTION"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>解決方案</h2></b></u><br>
				SPERO來填補原BITCOIN提案留下的空白。<br>
				我們引入PoS挖掘或股權證明，網絡選擇一個投資組合併在其中創建一個區塊。 在創建該塊時，您將獲得交易參與的獎勵。<br>
				這樣，你的平衡越大，被選中的機會就越大.<br>
				SPERO支付的保費相當於您的投資組合總價值每年<b> 25％</b>.<br>
				通過這種方式，您只需要一台運行SPERO軟件的計算機，並遵循以下一些簡單的規則：<br>
				<br><p style="text-align:left;">→ 保持在線投資組合最少每天2小時，最多12小時;<br>
→保持您的錢包上鎖參與; <br>
→您的平衡越高，您可以通過網絡的繪製越快; - <br>
→餘額等於您的網絡“重量”;<br>
→平衡大於1 SPERO <br> </p>
				這種採礦方式的優點很多，而最突出的就是參與採礦的可能性，而不必花費昂貴的設備。 你可以使用
例如舊的奔騰或RaspBerry！<br>
				這樣來的第二個好處就是：功耗低。<br>
				最初，我們提出我們項目的階段分配，按塊數劃分：<br>
				•從塊0到33331 = PoW和PoS挖掘階段; <br>
•從33332到263250 = PoS挖掘階段; <br>
•從上面的塊263251 = PoW和PoS挖掘階段<br>
