<p id="PROBLEM"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>THE PROBLEM</h2></b></u><br>
				Think of a world where you can buy your favorite products, your favorite food and still to conquer it, you do not have to spend the price that is imposed on that item or service with your current national currency ...<br>
				Would not that be interesting?<br>
				Be able to pay for your wife's dinner, buy your child's toy or snack at the door of your school or work without having to tinker with your monthly budget.<br>
				Since the year 2009 this is possible through a decentralized monetary system created by one (or a team) known worldwide by Satoshi Nakamoto. It was created from a source code developed by this being (or team)
an entirely different banking system from the conventional one, where all the transactions take place without any intermediary or bank delays due to their working hours.<br>
				BITCOIN was created to meet all these needs and best of all: it was available for free!<br>
				Anyone with a computer connected to the Internet could perform the known mining and obtain in exchange for the result of the labor test some coins provided by this system.<br>
				Mining consisted of solving mathematical problems and this resolution was done through processors that were used to their extreme to guarantee speed and accuracy in the answers.<br>
				Here begins the first problem:<br>
				Over the years the currency was getting more and more known and more users began to make use of this medium. However, with the growth of users, procurement has become increasingly difficult because there are always
improvements in processor fabrications and thus older processors and becoming obsolete.<br>
				Then began the race to obtain coins!<br>
				The newer your processor and the better your processing power, the faster you get the Satoshis (BITCOIN Fractional Unit). With this race, the market price began to leverage and each
more and more users arrived.<br>
				Here, then, a new modality begins: the participation of companies for professional mining (ASIC).<br>
				Companies began to develop machines with incredibly strong hardware and charged absurd prices for their acquisition.<br>
				Many users started buying these equipments and those who did not have the conditions, began to enter schemes known as "cloud mining" or mining in the cloud, where the big companies bought numerous ASICs and
with this they served the rent of the power of processing to its users.<br>
				 The counterpart of this scheme was first of all the very high risk of hiring a service that could never give you any return and in return if you bought the equipment the consumption of energy was very high,
often making mining damage.<br>
				To get an idea an ASIC S9 costs on average $ 2200 with a consumption of 1300W! With this we have the consumption superior to that of an air conditioner (1200W) !!!<br>
				In this way we sometimes have professional companies with more than 50 machines connected at the same time ... The consumption of electric energy is absurd and totally unsustainable ecologically speaking.<br>
				<br><p><img src="img/bitcoinfarm.jpg"><br><font style="font-size:12px">Professional Mining Center</font><br></p>
				Thus, only the rich get richer, waste their high costs that are often not paid and the end user is totally at the mercy of these entities.<br>
				Another major problem reported and experienced with the increase in the number of users of the BITCOIN network was the delay of confirmation of a transaction, or even the addition of this transaction in Blockchain.<br>
				A BITCOIN block can receive up to approximately 1.02Megabytes (day) or almost 292,359 transactions per day. To send your transaction to this block and be added to Blockchain you pay a fee known as "fee". The rate
BITCOIN standard today is 0.00001BTC, and if you pay a higher rate, your transaction has a higher priority than the others and will be added to the block faster.<br>
				Suggesting this, if the block were at number 292,358 and your transaction was the one that closed the block and someone sent a rate higher than yours, your transaction would only enter the next block.<br>
				With this, the expense with high rates is often unsustainable economically speaking and when it is sent with standard rate, the waiting time is gigantic (it can take more than 1 day).<br>
