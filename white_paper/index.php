<p id="SOLUTION"><div class="row form-group">
			<center style="text-align:center; font-size:18px;padding-top:5%">
								<b><u><h2>THE SOLUTION</h2></b></u><br>
				SPERO has come to fill in the gaps left by the original BITCOIN proposal.<br>
				We introduce PoS mining or proof of stake, where the network selects a portfolio and creates a block within it. When creating the block you receive a reward of participation of the transaction.<br>
				In this way, the higher your balance, the greater the chances of being selected.<br>
				The premium paid by SPERO is equivalent to <b> 25% </b> per year of the total value of your portfolio.<br>
				This way all you need is a computer running SPERO software and keeping it connected by following some simple rules:<br>
				<br><p style="text-align:left;">→ Keep the wallet online at least 2 hours a day and at most 12 hours;<br>
				→ Keep the wallet unlocked for participation;<br>
				→ The higher your balance, the faster you can be covered by the net draw;<br>
				→ The balance equals its "weight" of the network;<br>
				→ Balance greater than 1 SPERO<br></p>
				The advantages of this type of mining are numerous, and the most outstanding is the possibility of participating in this mining without having to spend money on equipment with high prices. You can use
that old Pentium or a RaspBerry for example!<br>
				In this way comes the second advantage: The low power consumption.<br>
				Initially we propose the distribution of phases of our project, being divided in number of blocks:<br>
				• From block 0 to 33331 = PoW and PoS mining phase;<br>
				• From block 33332 to 263250 = PoS mining phase;<br>
				• From block 263251 above = PoW and PoS mining phase<br>
				<br>
				In addition, we have the advantage of scalable or adjustable block according to the need of the network. Remember the example of BITCOIN? For it is our blocks have maximum time of 60 seconds and after this is recorded in the book.
If the network needs to use 292,360 transactions or more than 20MB all within 60 seconds, all transactions will be sent!<br>
				With this we gain agility in extremely fast transactions and compensation:<br>
				<br>
				<p>• 03 confirmations for user-to-user transactions;<br>
				• 05 confirmations for mined transactions<br></p>
				Thus the transactions have a maximum time of 5min to be registered in the block in the PoS mining phase and less than 3min in the hybrid mining phase.<br>
